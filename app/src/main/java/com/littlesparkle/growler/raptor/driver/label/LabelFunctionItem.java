public class LabelFunctionItem {
    public final int id;
    public final int labelIcon;
    public final int labelName;
    public final String url;

    public LabelFunctionItem(int id, int labelIcon, int labelName, String url) {
        this.id = id;
        this.labelIcon = labelIcon;
        this.labelName = labelName;
        this.url = url;
    }
}
